package com.example.youtubeadmin.Model;

public class TimePrediction {
    private String x;
    private int y;

    public TimePrediction(String x, int y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
